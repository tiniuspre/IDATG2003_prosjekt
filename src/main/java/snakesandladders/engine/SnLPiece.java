package snakesandladders.engine;

/**
 * The {@code SnakesAndLaddersPiece} enum represents the
 * different pieces that can be used in the Snakes and Ladders game.
 *
 * @author jonastomren
 * @version 15.04.2025
 * @since 15.04.2025
 * @see SnLPlayer
 */
public enum SnLPiece {
  /**
   * Enum value representing a hat.
   */
  HAT,
  /**
   * Enum value representing a car.
   */
  CAR,
  /**
   * Enum value representing a dog.
   */
  DOG,
  /**
   * Enum value representing a boot.
   */
  BOOT,
  /**
   * Enum value representing a thimble.
   */
  CAT,
  /**
   * Enum value representing a wheelbarrow.
   */
  WHEELBARROW
}
