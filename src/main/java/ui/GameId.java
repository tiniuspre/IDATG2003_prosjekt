package ui;

/**
 * Enumeration representing the different game IDs.
 * Used to identify various games in the application.
 *
 * @author tiniuspre
 * @version 25.04.2025
 * @since 25.03.2025
 */
public enum GameId {
  /**
   * Identifier for the Main menu.
   */
  MAIN_MENU,
  /**
   * Identifier for the Snakes and Ladders game.
   */
  SNAKES_AND_LADDERS,

  /**
   * Identifier for the Connect Four game.
   */
  CONNECT_FOUR,

  /**
   * Identifier for the Tic Tac Toe game.
   */
  TIC_TAC_TOE,
  /**
   * Identifier for the Settings menu.
   */
  SETTINGS
}
